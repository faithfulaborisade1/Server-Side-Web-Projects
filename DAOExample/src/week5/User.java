package week5;

public class User {
	private String name;
	private String address;
	
	
	public User(String name, String address) {
		
		this.name = name;
		this.address = address;
	}
	
	public String getname() {
		return this.name;
	}
	
	public String getaddress() {
		return this.name;
	}
	
	public void setname() {
		this.name = name;
	}
	
	public void setaddress() {
		this.address = address;
	}

}
